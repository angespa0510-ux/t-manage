-- Session 29: 操作マニュアルテーブル
CREATE TABLE IF NOT EXISTS ops_manual_categories (
  id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  icon text DEFAULT '📄',
  sort_order int DEFAULT 0,
  created_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS ops_manual_articles (
  id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  category_id bigint REFERENCES ops_manual_categories(id),
  title text NOT NULL,
  content text DEFAULT '',
  sort_order int DEFAULT 0,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- RLS
ALTER TABLE ops_manual_categories ENABLE ROW LEVEL SECURITY;
ALTER TABLE ops_manual_articles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "ops_cat_all" ON ops_manual_categories FOR ALL USING (true);
CREATE POLICY "ops_art_all" ON ops_manual_articles FOR ALL USING (true);

