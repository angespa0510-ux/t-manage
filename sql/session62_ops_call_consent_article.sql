通話AI機能で録音・AI分析を行うには、**お客様への事前告知が法的に必要**です。
無断録音は信頼関係を損なうだけでなく、トラブル時に守れません。

T-MANAGEでは、録音する通話だけ告知する運用ルールです。

## 基本方針

### 録音する通話
- 新規のお客様との通話
- 手動で録音ボタンを押した通話
- 要注意顧客との通話
- クレーム対応中に追加録音する場合

### 録音しない通話
- 常連様やリピーター様との短い予約変更電話
- セラピストや業者からの連絡

## 録音ボタンを押した時

[button:/call-test:🎙 通話AI画面へ]

録音ボタンを押すと、**同意確認ポップアップ**が表示されます。
ここに表示されるセリフを、そのままお客様にお伝えしてください。

### デフォルトのセリフ

> 「サービス品質向上のため、通話を録音させていただきます。ご了承ください」

お客様にお伝えしてから、ポップアップの「✓ 伝えました・録音開始」ボタンを押します。
これで「告知済み」として記録され、法的な証跡が残ります。

