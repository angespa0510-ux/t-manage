## お客様が録音を拒否された場合

1. すぐに録音停止ボタンを押す
2. お客様に「承知いたしました、録音は停止いたしました」と伝える
3. 必要に応じて通話内容を手書きメモで残す
4. T-MANAGEの通話AI履歴からも該当の録音を削除

## 通話の途中から録音を開始する場合

クレーム対応などで途中から録音が必要になった場合：

> 「ここからは、より正確にお話を伺うため、通話を録音させていただきます。
>  よろしいでしょうか？」

とお客様に確認してから録音を開始します。

## 言い忘れ防止のコツ

- CTIポップアップに表示されるセリフを**必ず見る習慣**をつける
- 最初の「お電話ありがとうございます」の後、必ず録音告知を入れる
- 新人さんは慣れるまで**セリフを書いた紙を電話横に置いておく**

## よくある質問

### Q. 常連様にも毎回告知が必要？
A. 一度告知済みのお客様には簡略化OKです。ただし、初めて録音する日は必ず告知してください。

### Q. セラピストからの電話も録音する？
A. 通常は不要です。業務連絡のみで個人情報のやり取りがないため。

### Q. お客様に録音について質問されたら？
A. 「サービス品質向上と研修用途で使わせていただいております。外部に提供することはありません。詳細はHPのプライバシーポリシーをご確認ください」とお伝えください。

