T-MANAGEに通話録音・AI文字起こし機能が追加されました。
現在はテスト運用中（Phase 1）の段階で、正式リリースに向けて検証を進めています。

### どんな機能？
・電話やBluetoothマイクからの音声を録音
・OpenAI Whisperで日本語に自動文字起こし
・結果はSupabaseに保存され、後から内容を確認できる
・「アンジュスパ」「チョップ」などの固有名詞も認識できるよう調整済み

### 使える人
・社長
・経営責任者

※一般スタッフのメニューには表示されません。内容に通話内容が含まれるため、最も厳しい権限設定にしています。

## アクセス方法

① 左メニューのハンバーガーから「🎙 通話AI」をクリック
② または直接 /call-test にアクセス

## テストの使い方

① 「🎙 通話AI」ページを開く
② ブラウザからマイク権限の許可を求められるので「許可」
③ Bluetoothスピーカーマイク「PowerConf S3」を使う場合は事前にPCとペアリングしておく
④ 「録音開始」ボタンを押す
⑤ 話し終わったら「録音停止」
⑥ 自動で文字起こしが始まり、結果がDBに保存される

### 録音のコツ
・静かな環境で録音する
・マイクから20〜30cm離れて話す
・早口にならないよう、はっきり話す
・固有名詞（コース名・地名など）はゆっくり発音する

## コスト・プライバシー

### コスト
・Whisper API: 1分あたり約 $0.006（約0.9円）
・想定月間コスト: 通話1,200件で ¥3,600〜6,000

### データの扱い
・音声データは保存しません（文字起こし後に破棄）
・テキストのみ Supabase に90日間保持
・保持期間を過ぎたテキストは自動削除

## よくあるトラブル

### マイクが認識されない
・ブラウザのマイク権限がONになっているか確認
・PowerConf S3 の電源が入っているか、ペアリング済みか確認
・ブラウザ設定 →「サイトの設定」→「マイク」で t-manage.vercel.app が許可されているか確認

### 文字起こしが始まらない
・録音時間が短すぎる可能性（3秒以上録音する）
・ネットワーク接続を確認
・ページをリロードして再度お試しください

### 文字起こしが不正確
・固有名詞は認識率が下がる場合があります
・録音環境が騒がしいと精度が落ちます
・極端に小さい声・早口は認識されにくいです

