'## マイページをアプリ化しよう

マイページは **「ホーム画面に追加」** することで、本物のアプリのように使えるようになります。

---

## なぜアプリ化するの？

ホーム画面に追加すると、以下のことができるようになります。

- 🔔 **プッシュ通知** で重要なお知らせを受け取れる（シフト確定・営業連絡など）
- ⚡ **アプリのように素早く起動** できる
- 🏠 **毎回ログインしなくてOK**
- 📱 **アドレスバーが消えて画面が広く** なる

---

## 📱 iPhone の場合（Safari で）

### ⚠️ 必ず Safari で開いてください
Chrome など他のブラウザではホーム画面に追加できません。

### ①Safari 下部の共有ボタンをタップ
画面下の真ん中にある、四角から上向きの矢印が出ているアイコンです。

### ②「ホーム画面に追加」を選ぶ
メニューが開いたら、**下にスクロール**して「➕ ホーム画面に追加」を見つけてタップします。

### ③右上の「追加」をタップ
確認画面が出るので、右上の「追加」ボタンをタップ。

### ④ホーム画面のアイコンから開く（ここが大切！）
Safari を閉じて、ホーム画面に追加された T-MANAGE のアイコンから開き直してください。Safari から直接開くと、通知などの機能が使えません。

---

## 🤖 Android の場合（Chrome で）

### ①画面右上の「⋮」メニューをタップ
Chrome のアドレスバー右にある、縦に点が3つ並んだアイコンです。

### ②「ホーム画面に追加」を選択
メニューから「ホーム画面に追加」または「アプリをインストール」をタップ。

### ③「追加」または「インストール」を確認
確認ダイアログが出るので「追加」をタップ。

### ④ホーム画面から開く
ホーム画面のアイコンから開いてください。

---

## 🔔 通知をオンにする

ホーム画面に追加したら、次は通知の設定です。

1. ホーム画面の **T-MANAGE** アイコンから起動
2. マイページにログイン
3. **🏠 ホーム**タブの「🔔 プッシュ通知」カードを探す
4. 「🔕 通知をオンにする」をタップ
5. 通知許可のダイアログで **「許可」** を選ぶ
6. ボタンが「🔔 通知オン」に変われば設定完了！

---

## ❓ よくある質問

### Q. ログイン情報は引き継がれる？
A. はい、Safari / Chrome でログインした情報は、ホーム画面のアイコンからも引き継がれます。

### Q. 容量は大きい？
A. わずか数MBで本物のアプリより軽量です。

### Q. アンインストールはどうやる？
A. ホーム画面のアイコンを長押しして「アプリを削除」を選ぶだけです。

### Q. 通知が来ない
A. iPhone の「設定」→「通知」→「T-MANAGE」で通知が許可されているか確認してください。iOS は必ず 16.4 以上が必要です。

### Q. プッシュ通知を止めたい
A. マイページの「🔔 プッシュ通知」カードから「タップで無効化」を押せばいつでも止められます。

