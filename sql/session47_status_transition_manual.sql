## 自動遷移の仕組み

以下の操作で**自動的にステータスが変わる**ため、通常スタッフが手動で変更する必要はありません。

### スタッフの操作で自動遷移

- **タイムチャートの「📩 SMS/LINE 送信」ボタンで「概要」モードを選び送信**
  → 未送信/WEB予約 → **概要送信済(未読)** に自動変更
- **同ボタンで「詳細」モードを選び送信**
  → それまでの状態 → **詳細送信済(未読)** に自動変更

### お客様の操作で自動遷移

- メッセージ内の「▼ ご予約確認はこちら」URLをタップ
  → 未読 → **既読** に自動変更（確認トークンで識別）
- お客様マイページから予約を確認
  → 同上

### セラピストの操作で自動遷移

- セラピストマイページの「開始」ボタン → **接客中**
- セラピストマイページの「終了」ボタン → **終了**

