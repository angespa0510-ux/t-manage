## 予約ステータスとは

T-MANAGEでは、1つの予約に **お客様向け** と **セラピスト向け** の2種類のステータスを持たせています。
これによって「誰が」「どこまで対応したか」が一目で分かります。

タイムチャートのバーに表示される色と短いラベルは、**お客様向けステータス**が元になっています。

