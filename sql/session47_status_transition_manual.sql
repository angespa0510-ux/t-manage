## セラピスト向けステータス（補助的）

セラピスト向けステータスは、セラピスト本人がマイページで操作するためのものです：

- `unsent` （まだセラピストに詳細連絡していない）
- `detail_sent` （詳細を送信済み）
- `serving` （接客中）
- `completed` （終了）

タイムチャート上では小さく「セ送信済」などで表示されます。

