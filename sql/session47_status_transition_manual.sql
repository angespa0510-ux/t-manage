## お客様向けステータス（主要な遷移）

```
📝 未送信
  ↓ 概要SMS/LINE送信
📨 概要送信済(未読)
  ↓ お客様が確認リンクをタップ
📬 概要送信済(既読)
  ↓ 詳細SMS/LINE送信
📩 詳細送信済(未読)
  ↓ お客様が確認
📭 詳細送信済(既読)
  ↓ セラピストが開始ボタン
🟢 接客中
  ↓ セラピストが終了ボタン
✅ 終了
```

### 各状態の詳細

| ラベル | 色 | 値 | 何が起きた状態か |
|---|---|---|---|
| 📝 未送信 | 灰 | unsent | 予約登録直後。まだ何も連絡していない |
| 🌐 WEB予約 | 紫 | web_reservation | WEB予約フォームから入った新規予約 |
| 📨 概要送信済(未読) | 青 | summary_unread | 概要メッセージ送信済み、お客様未確認 |
| 📬 概要送信済(既読) | 濃青 | summary_read | お客様が確認リンクをタップ |
| 📩 詳細送信済(未読) | 緑 | detail_unread | 場所・ルームなど詳細送信済み、お客様未確認 |
| 📭 詳細送信済(既読) | 濃緑 | detail_read | お客様が詳細確認済み |
| 🟢 接客中 | 緑 | serving | セラピストが開始ボタンを押した |
| ✅ 終了 | ベージュ | completed | セラピストが終了ボタンを押した |

