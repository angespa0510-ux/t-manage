## ステータス別にやること

### 📝 未送信（灰）
→ **まず「概要」を送る**ことから始めます。遅くとも前日夜までに。

### 📨 概要送信済(未読)（青）
→ お客様に届いているか、既読を待つ。しばらく既読にならない場合は電話確認。

### 📬 概要送信済(既読)（濃青）
→ **前日夜〜当日11時半までに「詳細」を送る**。

### 📩 詳細送信済(未読)（緑）
→ 時間までに既読にならない → 電話で念押し確認。

### 📭 詳細送信済(既読)（濃緑）
→ お客様は把握しているはず。セラピストの準備に集中。

### 🟢 接客中（緑）
→ 終了時刻を過ぎてもこのままなら、セラピストに状況確認。

### ✅ 終了（ベージュ）
→ [page:/timechart:タイムチャート] の「清算」ボタンから精算フェーズへ。

