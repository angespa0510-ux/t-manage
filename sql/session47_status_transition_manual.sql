## 対応漏れアラート

タイムチャート上で、以下の条件を過ぎた予約は自動でアラート通知が出ます。

| アラート種別 | 発動条件 |
|---|---|
| ⏰ 開始経過 | 予約開始時刻を過ぎても「接客中」でない |
| ⏰ 終了経過 | 予約終了時刻を過ぎても「終了」でない |
| 📨 概要未送信 | 開始◯分前になっても「概要送信済」でない |
| 📩 詳細未送信 | 開始◯分前になっても「詳細送信済」でない |
| 📞 電話確認 | 電話確認対象の予約で、開始◯分前を過ぎている |

**各アラートの時間やON/OFFは [page:/system-setup:システム設定] で変更できます。**

