## よくある質問

**Q. ステータスが自動で変わらないのですが？**
A. SMS/LINE送信ボタンから送った場合のみ自動更新されます。外部アプリで直接送った場合は変わりません。その場合は予約編集モーダルから手動で変更してください。

**Q. 既読になっているはずなのに「未読」のままです**
A. お客様がメッセージ内のURL（▼ ご予約確認はこちら）をタップしないと既読になりません。電話で口頭確認した場合は手動で変更してください。

**Q. 誤って「終了」にしてしまいました**
A. タイムチャートで予約をクリック→編集モーダルから「お客様向けステータス」を変更すれば戻せます。
