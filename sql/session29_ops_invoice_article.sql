インボイス制度に対応するため、適格事業者として登録しているセラピストには登録通知書の提出と登録番号の登録をお願いしています。

### リンクの発行方法（個別）
① セラピスト管理画面を開く
② 対象セラピストのカードで「❌ 適格」をタップ
③ URLが自動でクリップボードにコピーされる
④ LINEでセラピストにURLを送る

### リンクの一括発行
① セラピスト管理画面のヘッダーにある「📨 一括リンク発行」をタップ
② 未提出の全セラピスト分のリンクが一括で発行される
③ セラピストごとに「📋コピー」ボタンでURLをコピー
④ LINEで各セラピストに送る

### セラピスト側の操作（スマホ）
① LINEで届いたURLを開く
② 適格事業者登録番号を入力（Tから始まる13桁）
③ 登録通知書の写真を撮影またはファイル選択
④ 「📋 提出する」ボタンをタップ
⑤ 完了画面が表示される

### 提出完了後
・カード一覧のバッジが「✅ 適格」に変わる
・詳細画面に登録番号と通知書のサムネイルが表示される
・精算時のインボイス控除計算に自動反映される

### 一括リンク発行について
「📨 一括リンク発行」ボタンを押すと、以下の未提出書類のリンクがまとめて発行されます。
・📝 契約書（未契約のセラピスト）
・🪪 身分証（未提出のセラピスト）
・📋 適格事業者（未提出のセラピスト）
全員が提出済みの場合は「✅ 全員提出済み」と表示されます。

### 注意事項
・登録番号はTから始まる13桁の数字です（例: T1234567890123）
・番号が正しくない場合は提出できません（バリデーション付き）
・セラピストはログイン不要（トークン認証）
・全セラピストが適格事業者とは限りません。該当するセラピストのみ提出をお願いしてください', 7);
