[4]  大殿筋・腰部                5分
[5]  背中（広背筋・脊柱起立筋）   10分
[6]  肩・首・肩甲骨周辺           5分
[7]  仰向けへの体勢チェンジ       1分
[8]  脚の前面・足の甲             8分
[9]  腕・手                      5分
[10] デコルテ・首前面             3分
[11] 顔・頭皮（希望者のみ）       3分
[12] 仕上げ・お声かけ             1分
