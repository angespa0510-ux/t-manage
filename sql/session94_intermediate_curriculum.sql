# 体勢別の施術ポイント

施術中の **お客様の体勢（うつ伏せ・仰向け・横向き）** ごとに、効果的な施術ポイントと配慮事項が異なります。

## 1. うつ伏せの施術

### アクセスしやすい部位
- 背中全体（首〜腰）
- 肩甲骨周辺
- 腰・大殿筋
- 太ももの裏（ハムストリングス）
- ふくらはぎ
- 足底

### 体勢のセットアップ
1. **顔は施術ベッドの呼吸穴**に
2. 首が辛くないよう、必要に応じて頭部の高さを調整
3. **足首の下にロール**を入れて足首を楽に
4. 腕は楽な位置に（脇に・頭の上に・両脇に）

### 施術のポイント
- **背中の中央（脊柱）の真上は避ける** — 両脇の起立筋へ
- **腰部は弱めに** — 腎臓・内臓に近い
- **ふくらはぎは下から上へ** — リンパの流れに沿って
- **足底は土踏まずを中心に** — 踵骨は弱めに

### 配慮事項
- 妊娠中の方には禁忌（5ヶ月以降）
- 顔が辛そうな場合は休憩を入れる
- 長時間（30分以上）同じ体勢は避ける

## 2. 仰向けの施術

### アクセスしやすい部位
- 顔・頭皮
- デコルテ（鎖骨周辺）
- 腕（上腕・前腕）
- 腹部（適切な場合のみ）
- 太ももの前・側面
- 脛・足の甲

### 体勢のセットアップ
1. **膝の下にロール**を入れて膝・腰を楽に
2. **首の下にタオル**で首のカーブをサポート
3. 腕は **手のひらを上向き** に（リラックスしやすい）
4. 体が冷えないようにブランケットをかける

### 施術のポイント
- **顔・頭皮は最も繊細** — 軽い圧で
- **デコルテはリンパが集中** — 鎖骨に向けて流す
- **腹部は時計回り** — 大腸の流れに沿う、強圧禁止
- **大腿前面（大腿四頭筋）はしっかり** — 大きな筋肉

### 配慮事項
- お腹を出すことに抵抗があるお客様も多い → タオルで隠す
- 顔のオイル使用前にアレルギー再確認
- メイクをしたまま施術を希望される場合は顔を避ける

## 3. 横向きの施術

### 用途
- 妊娠中のお客様（5ヶ月以降）
- 腰痛が強くうつ伏せ困難な方
- 高齢のお客様

### アクセスしやすい部位
- 上側の肩・首
- 背中（片側）
- 上側の腰・腸骨稜
- 上側の脚

### 体勢のセットアップ
1. **横向きに寝てもらう**
2. **頭の下に枕** で首のラインを保つ
3. **両膝の間にクッション** — 骨盤の歪み予防
4. **抱き枕** で安定感を出す（妊婦さんに特に推奨）
5. **下側の腕は前に** 出して圧迫を避ける

### 施術のポイント
- **上側の肩甲骨周辺**にアクセスしやすい
- **腸骨稜（骨盤の上端）** からの腰アプローチ
- 反対側にも同様に行うため、**途中で体勢を変えてもらう**

### 配慮事項
- 体勢チェンジ時に転倒注意（特に妊婦・高齢者）
- 妊娠中は **左側を下にする** ことが推奨される（下大静脈圧迫を避けるため）
- 5ヶ月未満の妊婦さんは念のため施術自体を控える

## 4. 体勢チェンジのスムーズな誘導

### 言葉かけの例
- 「次にうつ伏せになっていただきますね、ゆっくり起き上がってください」
- 「お顔は呼吸の穴にお願いします」
- 「足の下にロールを入れますね、楽な位置はいかがですか？」

### サポート
- ベッドの上で寝返りを打つように誘導（起き上がる必要なし）
- 高齢の方・妊婦さんは **手を取ってサポート**
- 不調・めまいがないか確認

