## 2. 各セクションの詳細

### [1-2] スタート（6分）
- お客様にうつ伏せになっていただく
- ブランケットで体を覆い、施術部位だけ露出
- オイルをセラピストの手に温めてからなじませる
- **足首** から開始することで「これから始まる」サインに

### [3] ふくらはぎ・太もも裏（8分）

```
ふくらはぎ:
  摩擦法 → 揉捏法 → 圧迫法 → 摩擦法
  リンパ流れに沿って 足首→膝へ

太もも裏:
  膝裏 → 太ももの中央 → 大殿筋付近
  揉捏法・圧迫法を中心に
```

**アキレス腱は弱めに**、**膝裏は軽くタッチ程度**

### [4] 大殿筋・腰部（5分）

- 大殿筋は **大きな筋肉なので強めOK**（圧4〜5）
- 腰部は **弱め**（圧2〜3）
- 仙骨周辺は手のひら全体で温めるイメージ
- **腎臓部位（背中側の腰）は強圧禁止**

### [5] 背中（10分・施術の中心）

```
[A] 全体の摩擦法でオイル広げる    1分
[B] 広背筋の揉捏法（外側から）    3分
[C] 脊柱起立筋の圧迫法            4分
[D] 肩甲骨周辺の振動法            1分
[E] 軽い叩打法で仕上げ            1分
```

**脊柱の真上は絶対に押さない** — 必ず両脇の起立筋へ

### [6] 肩・首（5分）

- 肩甲挙筋（首から肩甲骨上部）は **多くの方が凝っている**
- 後頭下筋群は軽くタッチ程度
- 首の前面は触らない（動脈・甲状腺がある）

### [7] 体勢チェンジ（1分）

```
セラピスト: 「ではゆっくり仰向けになってください」
       (バスタオルで体を覆って隠しながら誘導)
       「お首の下にタオルを入れますね」
       「お膝の下にロールを入れますね」
```

