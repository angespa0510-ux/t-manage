# 摩擦法・振動法・叩打法の応用

圧迫法・揉捏法に続く、3つの補助技法を学びます。これらを使い分けることで施術の幅が広がります。

## 1. 摩擦法（まさつほう / 軽擦法）

### 概要
手のひらでさすって **皮膚と浅層の血流を促進** する手技。施術の **始まり・終わり・部位の切り替え** に使います。

### 基本動作
1. 手のひら全体を肌に密着させる
2. 適度な圧（レベル2〜3）で滑らせる
3. **末梢から中枢へ** の方向（脚なら足首→太もも）
4. オイルを使う場合はここで広げる

### ポイント
- **オイルとなじませる役割**を兼ねる
- 摩擦の速度で雰囲気が変わる
  - 遅い → リラックス
  - 速い → 元気・活性化
- 長時間の連続摩擦は熱を生じるので3〜5分に留める

### 適応場面
- **施術の最初** — オイルをなじませながら筋肉を温める
- **施術の最後** — 仕上げの整え
- **部位移動時** — 圧迫法 → 移動 → 摩擦法 → 次の圧迫法

## 2. 振動法（しんどうほう）

### 概要
手や指で **細かく震わせる** 手技。深部の筋肉に振動を伝え、緊張を緩めます。

### 基本動作
1. 母指または手のひらを当てる
2. 圧をかけたまま、**手首から細かく震わせる**
3. 3〜10秒継続
4. 同じ位置で繰り返す

### ポイント
- **腕全体で振るのではなく手首・指先で振る**
- 強すぎる振動は逆に痛い → 軽めに
- 神経が走る部位（坐骨神経領域等）で活用

### 適応部位
- 肩甲骨周辺（深部筋）
- 仙骨・腸骨稜
- 頭部（軽い振動でリフレッシュ）

### 注意
- 神経の真上は **強い振動を避ける** — しびれを誘発する可能性
- 妊娠中のお客様の腹部は振動禁止

## 3. 叩打法（こうだほう）

### 概要
手や指で **リズミカルに叩く** 手技。筋肉に刺激を与え、活性化させます。

### 種類

| 種類 | 手の形 | 用途 |
|---|---|---|
| 切打（せつだ） | 小指側で叩く | 背中全体 |
| 拳打（けんだ） | こぶしの軽い面で叩く | 大殿筋 |
| 鼓打（こだ） | 手のひらをカップ状に | 太もも・ふくらはぎ |
| 指尖叩打 | 指先で軽く叩く | 顔・頭皮 |

### 基本動作
1. 手首を柔らかく保つ
2. **力を抜いて** 自重で落とす
3. 1秒に4〜6回のリズム
4. 5〜10秒で次の部位へ

### ポイント
- **腕で叩かない、手首のスナップで叩く**
- 力ではなくリズムで刺激
- 連続しすぎると痛い → 短時間で複数部位に

### 適応場面
- 施術の終盤 — 終わり感を演出
- 元気を出したいお客様向け
- 朝の施術（活性化目的）

### 禁忌
- 骨の上（脊柱・肋骨・関節）
- 内臓の真上（腎臓部・腹部）
- 高齢者・妊婦
- 高血圧の方の頸部・頭部

## 4. 5つの基本手技まとめ

| 手技 | 速度 | 圧 | 主目的 |
|---|---|---|---|
| 摩擦法 | 中 | 弱〜中 | 表層の血流・なじませ |
| 揉捏法 | 中 | 中 | 筋肉の血流促進 |
| 圧迫法 | 遅 | 中〜強 | 深部の緊張緩和 |
| 振動法 | 細かい | 中 | 深部の緊張緩和 |
| 叩打法 | 速 | 弱〜中 | 活性化・仕上げ |

## 5. 標準施術での組み合わせ例

```
[1] 摩擦法でオイルなじませ・準備     (3分)
   ↓
[2] 揉捏法で全体の血流促進          (5分)
   ↓
[3] 圧迫法で深部のコリにアプローチ    (10分)
   ↓
[4] 振動法で頑固なコリに            (3分)
   ↓
[5] 揉捏法で再度ほぐす             (5分)
   ↓
[6] 叩打法で仕上げ・覚醒            (1分)
   ↓
[7] 摩擦法で整える                 (3分)
```

合計30分の施術では、これらをバランスよく配置します。お客様の状態（リラックス目的 / 活性化目的）で配分を変えます。
