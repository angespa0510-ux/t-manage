# リンパドレナージュの基本手技

リンパドレナージュは **通常のボディケアとは「圧」も「方向」も異なる** 専用技法です。必ず必須カリキュラムの「リンパ系の理解」修了後に取り組んでください。

## 1. リンパドレナージュの特徴

| 項目 | 通常ボディケア | リンパドレナージュ |
|---|---|---|
| 圧 | 中〜強（レベル3〜5） | **非常に弱い**（レベル1〜2） |
| 速度 | 中 | **ゆっくり**（1秒1動作） |
| 方向 | 双方向 | **末端→中心へ一方向** |
| 主目的 | 筋肉の緊張緩和 | リンパ液の循環促進 |
| 対象層 | 筋肉（深部） | 皮膚直下のリンパ管 |

リンパ管は **皮膚のすぐ下** を走っています。強く押すとつぶれてしまい、逆効果になります。

## 2. 基本手技4種

### ① 静止圧（じょうし圧 / ストロークアウェイ）

```
両手を皮膚に置き、軽く押し込んで5秒キープ → ゆっくり離す
```

- リンパ節周辺で使用
- 「リンパの出口を開く」イメージ

### ② ポンプ法

```
手のひらを密着、手首を起点にゆっくり押し→離すを繰り返す
1秒1回のリズムで5〜10回
```

- 鎖骨上・腋窩・鼠径部のリンパ節で使用
- ポンプのように **一定のリズム** が重要

### ③ ロータリー法（円を描く）

```
手のひらまたは指の腹で、皮膚の上に直径3〜5cmの円を描く
中心に向かう方向で、1秒1周
```

- 広い面積（背中・腹部・大腿部）に
- リンパを **「巻き込む」** イメージ

### ④ ストローク法（流す）

```
手のひら全体で、末端から中心へ向けて滑らせる
圧は皮膚に痕がつかない程度
1秒で15〜20cm進む
```

- 仕上げ・部位移動時に
- **大きく長く** 流す

## 3. 施術の基本順序

リンパドレナージュは **「出口を開けてから流す」** が鉄則です。

```
[1] 鎖骨リンパ節を開く（最終出口）
   ↓
[2] その日の施術部位の最寄りリンパ節を開く
   (脚なら鼠径部、腕なら腋窩、顔なら顎下)
   ↓
[3] 末端から該当リンパ節へ流す
   ↓
[4] 仕上げに鎖骨リンパ節へ向けて全身を流す
```

「**出口を開ける前に流すと、行き場のないリンパ液が滞る**」と理解してください。

## 4. 圧の調整 — 「皮膚を伸ばす程度」

リンパドレナージュの圧は表現が難しいですが、目安は:

> **「皮膚をわずかに伸ばす程度」**
> **「自分の腕の上で試して、皮膚が動くけれど痛くない圧」**

10円玉が乗っている程度の重さ、と教える流派もあります。

### 圧が強すぎるサイン
- お客様が「効いてる」と感じる → 通常ボディケアの圧
- 皮膚が赤くなる
- 痛みを感じる

これらは全て圧の強すぎサイン。意識して **「これで効いているか不安」** くらいまで弱めます。

## 5. 速度の重要性

リンパ液は **血液の100分の1の速度** で流れます。施術もそれに合わせます。

- ❌ 通常ボディケアの速度（1秒1〜2動作）
- ✅ リンパドレナージュの速度（**3秒1動作** が目安）

「焦らない・急がない・優しく」を体に覚え込ませる必要があります。

## 6. 効果が出るまでの目安

| 効果 | 出現時期 |
|---|---|
| 一時的なむくみ軽減 | 施術直後 |
| 排尿量の増加 | 当日中 |
| 体の軽さ・スッキリ感 | 翌朝 |
| 慢性むくみの改善 | 4〜8回継続後 |

施術後の **水分摂取** をお客様に勧める（「老廃物を流すために」）

## 7. 禁忌事項（必ず守る）

以下のお客様には **施術を行わない**:

- 発熱・体調不良
- 急性の炎症（赤み・腫れ・熱感）
- 皮膚の感染症（水疱・湿疹・ヘルペス等）
- リンパ系の医学的治療を受けている方
- 心臓疾患・腎臓疾患のある方
- 妊娠初期（〜16週）
- 当日の飲酒後
- 食後30分以内（軽い食事なら可、満腹はNG）

判断に迷う場合は **施術を控え、医師相談を勧める** こと。

## 8. お客様への伝え方

リンパドレナージュは「優しすぎる」と感じるお客様も多いです。事前に説明:

> 「リンパドレナージュは通常のマッサージと違い、**わざと優しい圧** で行います。リンパは皮膚のすぐ下を流れているので、強い圧では逆に流れが滞ってしまうためです。物足りなく感じられるかもしれませんが、施術後の体の軽さでご実感いただけます」

期待値を擦り合わせることが、満足度向上の鍵です。
