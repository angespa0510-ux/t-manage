## 1. 顔・デコルテのリンパ流れ

```
[起点] 顔の各部位
   ↓
[1] 顎下リンパ節（顎の下）
   ↓
[2] 耳下腺リンパ節（耳の前）
   ↓
[3] 頸部リンパ節（首の側面）
   ↓
[4] 鎖骨リンパ節（鎖骨上下） ← 最終出口
   ↓
[5] 鎖骨下静脈で血液合流
```

**全ての顔のリンパは最終的に鎖骨へ集まる** ので、デコルテケアと一体で行うのが効果的です。

