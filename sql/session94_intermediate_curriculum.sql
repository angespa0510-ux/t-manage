# 圧迫法と揉捏法の基本

ボディケアの基本となる二大手技、**圧迫法（あっぱくほう）** と **揉捏法（じゅうねつほう）** を学びます。すべての施術はこの2つの応用です。

## 1. 圧迫法（あっぱくほう）

### 概要
**一定の圧を一定時間かけ続ける** 手技。筋肉の深部に持続的な刺激を与え、緊張を緩めます。

### 基本動作
1. 手のひら全体・母指（親指）・四指の腹を使う
2. 圧をかける位置を決め、**ゆっくり**沈み込む
3. 3〜5秒キープ
4. 同じ速度で **ゆっくり**離す

### ポイント
- **垂直に圧をかける** — 斜めの圧は痛みやすい
- **呼気に合わせる** — お客様の息を吐くタイミングで深く入る
- **左右対称に行う** — 体の歪みを助長しない

### 適応部位
- 肩・首（僧帽筋・肩甲挙筋）
- 背中（脊柱起立筋・広背筋）
- 腰（腰方形筋）
- 大殿筋（坐骨周辺）

### 禁忌
- 骨の真上（脊柱・肋骨・関節）
- 動脈拍動部（首の前面・鼠径部中央）
- 腫れ・炎症のある部位

## 2. 揉捏法（じゅうねつほう）

### 概要
**筋肉をつかんで揉む** 手技。圧迫法と異なり、動きを伴って血流を促進します。

### 基本動作
1. 母指と他の四指で筋肉を挟む（または手のひらで包む）
2. 筋肉を持ち上げるように軽くつかむ
3. 揉む・回す・絞る動作を組み合わせる
4. **筋肉の走行に沿って** 移動

### ポイント
- **皮膚をこすらない** — 筋肉ごと動かすイメージ
- **リズムを保つ** — 一定のテンポ（1秒1動作目安）
- **指先ではなく指の腹を使う** — 爪が当たらない

### 適応部位
- 肩・腕（三角筋・上腕二頭筋）
- 太もも（大腿四頭筋・ハムストリングス）
- ふくらはぎ（腓腹筋・ヒラメ筋）

### 注意点
- 関節周辺は揉まない（関節は圧迫法で）
- 高齢のお客様には弱めに（皮膚・血管が脆くなっている）

## 3. 圧迫法と揉捏法の組み合わせ

実際の施術では2つを組み合わせます。

```
[1] 圧迫法で深部の緊張を緩める  (3〜5分)
   ↓
[2] 揉捏法で血流を促進する  (5〜8分)
   ↓
[3] 再度 圧迫法で深部に届かせる  (3〜5分)
   ↓
[4] さする動作（軽擦法）で仕上げる
```

## 4. 圧の段階表現

お客様への圧の確認は **5段階** で統一すると伝わりやすいです。

| レベル | 表現 | 目安 |
|---|---|---|
| 1 | ソフト | 触れているだけ |
| 2 | やや弱め | 表面だけ |
| 3 | 普通 | 筋肉表層に届く |
| 4 | しっかり | 筋肉中層〜深部に届く |
| 5 | 強め | 深部にしっかり届く（痛気持ち良い） |

「6以上」は **基本的に存在しない**。痛みを我慢させる施術は施術業として不適切です。

## 5. 練習の方法

### セルフ練習
- 自分の太ももで圧の感覚を確認
- スポンジ・クッションで持続圧の練習

### ペア練習
- 同僚同士でフィードバック
- 「圧の強さ」「リズム」「位置の正確さ」を指摘し合う

### 反復ポイント
- まず **片手で正確にできる** ようにする
- 次に **両手で対称に** できるようにする
- 最後に **動きながら（移動しながら）** できるようにする
