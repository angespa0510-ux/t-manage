# 反射区とフットケア応用

足裏の反射区を活用したフットケアは、独立した付加メニューになる専門技術です。基本的な反射区マップと施術法を学びます。

## 1. 反射区とは

### 概念

反射区（リフレクソロジー）は、**足の特定の部位が体の各臓器・器官と対応している** という考え方に基づくケア法です。

### 施術業としての立ち位置

⚠ 重要: 反射区ケアは医療技術ではありません。施術業として:

- ✅ リラクゼーション・足の疲労改善
- ✅ 「気持ちいい」「リフレッシュした」を目的とした施術
- ❌ 病気の治療
- ❌ 「○○の臓器が悪い」等の診断
- ❌ 医学的効能の主張

「反射区＝臓器の調子がわかる」のような断定はせず、**「気持ちよさ」を主軸** に提供します。

## 2. 足裏反射区の基本マップ

### 基本配置

足裏全体を **「人体の縮図」** として捉えます。

```
[つま先側]  → 頭部・顔・首
[足の指]    → 脳・目・耳・歯
[母指球]    → 肺・心臓 (左足) / 肝臓 (右足)
[土踏まず]  → 内臓 (胃・腎臓・腸)
[かかと側]  → 骨盤・腰・坐骨
[足の側面]  → 背骨・関節
```

### 主要な反射区

| 反射区 | 位置 | 対応器官 |
|---|---|---|
| 頭部 | 親指の腹 | 頭・脳 |
| 目 | 人差し指・中指の付け根 | 目 |
| 肩 | 小指の下 | 肩・首 |
| 肺 | 母指球（拇指の下のふくらみ） | 呼吸器系 |
| 心臓 | 左足の母指球の下 | 循環器系 |
| 肝臓 | 右足の土踏まず上部 | 肝臓 |
| 胃 | 両足の土踏まず中央 | 消化器系 |
| 腎臓 | 土踏まずの中央やや上 | 泌尿器系 |
| 腸 | 土踏まずの下半分 | 消化器系 |
| 骨盤 | かかと | 骨盤・生殖器 |
| 腰椎 | 内側のアーチに沿って | 腰 |
| 坐骨神経 | かかとの内側 | 坐骨神経 |

