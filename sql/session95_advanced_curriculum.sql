# 脚部・むくみの集中ケア

立ち仕事・デスクワークの方の悩みである「脚のむくみ・疲労」に特化した専門アプローチ。リンパドレナージュとボディケアの統合技術です。

## 1. 脚のむくみ・疲労の原因

### 体液の流れの仕組み

```
[心臓] → [動脈] → [毛細血管] → [細胞]
                              ↓
                         [組織液]
                              ↓
[心臓] ← [静脈] ← [毛細血管] ← [細胞]
              ↘
             [リンパ管] (余分な水分・老廃物)
```

立ち仕事・座り仕事で **筋肉が動かない** と、ふくらはぎの「ミルキングアクション」（筋肉の収縮ポンプ）が働かず、体液が下半身に溜まります。

### むくみの段階

| 段階 | 症状 | 改善期間 |
|---|---|---|
| 一時的 | 夕方の脚の重さ | 一晩で改善 |
| 慢性 | 朝も解消されない | 数日〜1週間 |
| 重度 | 押すと跡が残る | 専門医療領域 |

**「押すと跡が残る」**「片足だけ極端に腫れる」 場合は医療領域。施術業として施術せず受診を勧めます。

## 2. 脚部の解剖学的理解

### 主な筋肉

| 筋肉 | 位置 | 機能 |
|---|---|---|
| 大腿四頭筋 | 太もも前 | 膝を伸ばす |
| ハムストリングス | 太もも裏 | 膝を曲げる |
| 内転筋群 | 太もも内側 | 脚を内側へ |
| 大腿筋膜張筋 | 太もも外側 | 大腿を外側へ |
| 腓腹筋 | ふくらはぎ表層 | 足首を下げる |
| ヒラメ筋 | ふくらはぎ深部 | 足首を下げる |
| 前脛骨筋 | 脛の外側 | 足首を上げる |

### 主なリンパ節

```
[膝裏] 膝窩リンパ節 → 下腿のリンパを受ける
   ↓
[太ももの付け根] 鼠径リンパ節 → 下半身全体のリンパを受ける
   ↓
[腰部・腹部] → 体幹リンパへ
   ↓
[最終] 鎖骨リンパ節
```

