[Step 2] 香り選定                        2分
[Step 3] うつ伏せ — 背中全体準備        8分
[Step 4] うつ伏せ — 脊柱起立筋（腰部）  10分
[Step 5] うつ伏せ — 腰方形筋・側面       5分
[Step 6] うつ伏せ — 大殿筋・中殿筋      10分
[Step 7] うつ伏せ — 梨状筋（慎重に）     5分
[Step 8] うつ伏せ — 大腿裏              7分
[Step 9] 体勢チェンジ                    1分
[Step 10] 仰向け — 腸腰筋（腹部から）    5分
