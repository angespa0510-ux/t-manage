4. ゴルフボールで足裏ころころ
5. 足湯 (週1〜2回・40°Cで15分)
6. 足を冷やさない (5本指ソックス推奨)
```

→ 「医療的な指導」ではなく「快適に過ごすコツ」として伝える。

## 10. フットケアの位置付け

### 単独メニューとして

- 30分・60分のフットケア専門コース
- 妊婦さん向けの足裏ケア（医師相談済みの方）
- 高齢者向けのリラクゼーション
- 立ち仕事の方の定期メンテナンス

### 全身施術への組み込み

- 60分全身施術の最後に5分の反射区
- リラクゼーション系コースの締め
- 不眠改善コースに10分追加

### 単独メニューの利点
- 妊娠中の方にも提供しやすい（条件付き）
- 着衣のままで施術可能（施術業として安心感）
- 短時間でも効果を体感しやすい

## 11. リフレクソロジーとの違い

### 反射区ケア（施術業として）
- リラクゼーション目的
- 気持ちよさの追求
- アロマと組み合わせやすい

### 専門リフレクソロジー（外部資格者の領域）
- 体系化された認定資格
- 反射区を体の状態の判断にも使う
- 医療従事者と連携することも

施術業として深めたい場合は、外部の専門認定資格取得もキャリアの一つです（業務委託契約書 第10条 研修受講義務に基づく自己研鑽）。

