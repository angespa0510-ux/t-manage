## 3. 肩甲骨周辺の徹底アプローチ（Step 4）

### 「肩甲骨剥がし」の感覚

肩甲骨は **本来動きやすい骨** ですが、デスクワークで肋骨に張り付いたように固まります。これを剥がしていくのが集中ケアです。

### 手順

```
[1] 肩甲骨の輪郭を指でなぞる (1分)
   位置を正確に把握する

[2] 肩甲骨の内側 (脊柱側) のキワを押圧 (3分)
   菱形筋の緊張を緩める
   親指で深部までゆっくり

[3] 肩甲骨の上縁 (首側) のキワを押圧 (3分)
   僧帽筋上部・肩甲挙筋を緩める

[4] 肩甲骨の外側のキワを押圧 (3分)
   棘下筋の緊張を緩める
   四十肩・五十肩予防にも効果

[5] 肩甲骨を浮かせる (3分)
   お客様の腕を内側に背中側に回していただく
   → 肩甲骨が浮き上がる
   → そのスキマに指を入れて持ち上げる
   ※ お客様が嫌がる場合は無理しない

[6] 仕上げ — 肩甲骨周辺全体を温める (2分)
```

### 注意
- **肋骨を圧迫しない** — 横から見て肋骨の位置を意識
- **痛みを我慢させない** — 「気持ちいい範囲」で
- **左右差を確認** — 利き手側が固いことが多い

## 4. 首・後頭部のケア（Step 5）

### 後頭下筋群へのアプローチ

「頭が痛い」「頭が重い」は後頭下筋群の緊張が原因のことが多い。

```
[1] 後頭部の根本（うなじ）を指で探る
   骨と筋肉の境目を見つける

[2] そこから 1〜2cm 下の左右の窪みを軽く押す
   親指の腹で、垂直にゆっくり

[3] 5秒キープ → ゆっくり離す

[4] 5回繰り返す
```

### 首後ろ全体

```
[1] 髪の生え際から肩まで、両手で挟むように
[2] 揉捏法で全体をほぐす
[3] 後頭部から肩へストローク
```

### 注意
- **首の前面は触らない**（動脈・甲状腺）
- 椎骨の真上は強く押さない
- お客様にめまい・吐き気がないか時々確認

## 5. 仰向け — 大胸筋・小胸筋（Step 7）

### 「巻き肩」の解消が鍵

```
[1] 鎖骨の下を、内側 → 外側に指圧
   大胸筋の起始部を緩める

[2] 大胸筋を全体的に揉捏
   ※ 女性のお客様は バスト付近を避ける

[3] 鎖骨の下の深部 (小胸筋) を慎重に押す
   神経・血管が走るので 弱め に

[4] 肩関節を動かしながら 大胸筋をストレッチ
   お客様の腕を 万歳の位置 に
   そのまま 1分キープ
```

### お客様の体感
- 「胸が開いた感じがする」
- 「呼吸が深くなった」
- 「姿勢が変わった気がする」

## 6. 顔・頭皮（Step 10）

