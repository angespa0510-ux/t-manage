## 4. 痛み位置の確認（Step 1）

### お客様への質問

```
[1] 「腰の どのあたり が痛みますか？」
    (中央 / 右側 / 左側 / 全体)

[2] 「動かして痛いですか？じっとしていても痛いですか？」
    (動作時痛 / 安静時痛)

[3] 「足にしびれや痛みは出ますか？」
    (神経症状の確認 — 出る場合は要注意)

[4] 「医療機関で診断を受けたことはありますか？」
    (ヘルニア等の既往確認)

[5] 「何か思い当たる原因はありますか？」
    (運動・仕事・ストレス等)
```

### 図示してもらう

「お客様、痛い場所を指で指していただけますか？」と、自分の体で示してもらう。**両側か片側か**、**中央か外側か** で原因の見当がつきます。

## 5. 脊柱起立筋への深部アプローチ（Step 4）

### 体勢
- うつ伏せ
- お腹の下に薄いクッション（腰のカーブを軽減）
- 足首の下にロール（足首をリラックス）

### 手順

```
[1] 腰部全体に軽い圧で温める (1分)
   オイルを広げながら

[2] 背骨の両脇 1.5cm のラインを 仙骨〜肩甲骨方向 にストローク (2分)
   両親指で

[3] 腰椎レベルの脊柱起立筋を 圧迫法 (3分)
   3秒押す→離す を繰り返し
   左右交互にゆっくりリズミカルに

[4] 凝りの強い部位は 振動法 (2分)
   親指で深部まで届かせる

[5] 全体を揉捏法で緩める (2分)
```

### 重要ポイント
- **脊柱の真上は絶対に押さない**
- 起立筋の **走行方向に沿って** アプローチ
- 圧は中〜強でも、急激な圧は禁忌

## 6. 大殿筋・中殿筋・梨状筋（Step 6-7）

### 大殿筋

```
[1] お尻全体を 手のひらで包むように 揉捏法 (2分)
[2] 仙骨の外側、坐骨の上のラインを圧迫 (2分)
[3] お尻の中央〜外側を体重をかけて圧迫 (3分)
   ※ 親指または肘を使用
[4] 軽い叩打法で仕上げ (1分)
```

### 中殿筋

```
[1] お尻の上端 (腸骨稜の下) のラインを圧迫 (1分)
[2] そのまま側面に下りていく (1分)
   股関節の外側 (大転子の上方)
```

### 梨状筋（坐骨神経痛様症状の原因筋）

```
[1] 大殿筋の中心から少し下を、深部まで圧迫
   親指または肘でゆっくり
   3秒押す → 離す
   
[2] お客様にしびれが出ないか確認
   → 出る場合は すぐ中止 (神経への直接圧迫の可能性)
```

### ⚠ 梨状筋の注意

梨状筋の真下を **坐骨神経が通っています**。強い圧迫は神経を圧迫し、しびれや痛みを誘発する可能性。

- 圧は **中** 程度に
- お客様の反応を **常に確認**
- しびれが出たら即中止
- 慢性的な坐骨神経痛のお客様には施術しない

## 7. 腸腰筋へのアプローチ（Step 10・上級）

### 腸腰筋とは

お腹の深部、腰椎から大腿骨につながる筋肉。**反り腰の原因** で、慢性腰痛の隠れた犯人。

### 体勢
- 仰向け
- 膝の下に大きめのロール（腰の負担軽減）

### 手順（上級者向け・慎重に）

```
[1] お客様にお腹を出していただく
[2] おへその外側 3〜4cm のラインを軽く押す
[3] そこから 太ももの付け根 (鼠径部) に向けて
   ゆっくり 深部まで圧
[4] お客様に深呼吸をしていただきながら
   呼気時に深く沈み込ませる
[5] 5秒キープ → ゆっくり離す
[6] 3回繰り返す
```

### 注意
- お腹は **強圧禁忌**
- 食後直後は施術しない
- 妊娠の可能性がある方は禁忌
- 腹部大動脈を直接圧迫しない（中央は避ける）
- 「腸腰筋アプローチ」は経験を積んでから

## 8. 仕上げ・姿勢チェック（Step 12）

### 起き上がり時の確認

```
セラピスト: 「ゆっくり起きてください」
       「立ってみてください」
       「腰の感じはいかがですか？」
       「前屈してみてください」
       「左右にひねってみてください」
```

### 効果の体感ポイント
- 「体が軽くなった」
- 「腰が伸びる感じ」
- 「動きがスムーズ」

## 9. 自宅セルフケア・アドバイス

### 施術業として伝えられること

```
1. 入浴で温める (シャワーだけで済ませない)
2. 寝る時の姿勢
   - 横向き寝なら膝の間にクッション
   - 仰向け寝なら膝の下に枕
3. 立ち仕事の合間の腰回し
4. 椅子の選び方 (深く座って背もたれを使う)
5. 重い物を持ち上げる時の姿勢 (膝を使う)
```

### 伝えてはいけないこと
- ❌ 「ヘルニアではないですよ」（医療判断）
- ❌ 「これで治ります」（治療と誤解される）
- ❌ 「○○のストレッチを毎日」（過度な指導）

→ 施術業の範囲を超えない。

## 10. 受診を勧めるべき症状

施術中・施術後に以下のサインがあれば、医療機関の受診を勧めます:

- 足にしびれ・痛みが走る
- 立ち上がる時に激痛
- 排尿・排便に異常
- 急激な痛みの増悪
- 発熱を伴う
- 体重減少を伴う

「念のため、一度整形外科でご相談されてみてはいかがでしょうか」と優しく勧めるのが正しい対応です。

## 11. 継続施術プログラム

### 急性期回復後の集中ケア
- 週1〜2回 × 6週間
- 自宅セルフケアと併用

