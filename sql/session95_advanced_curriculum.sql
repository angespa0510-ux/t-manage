# 肩・首の集中ケア（デスクワーク疲労）

現代人の悩みの代表「肩こり・首こり」。デスクワーク中心の生活様式が原因です。本モジュールでは肩・首特化の専門アプローチを学びます。

## 1. デスクワーク疲労の解剖学的理解

### 主な原因筋

| 筋肉 | 位置 | 役割 | 疲労の出方 |
|---|---|---|---|
| 僧帽筋上部 | 首〜肩 | 肩甲骨を引き上げる | パンパンに張る |
| 肩甲挙筋 | 首〜肩甲骨上角 | 肩甲骨を持ち上げる | こわばる |
| 菱形筋 | 肩甲骨内側 | 肩甲骨を引き寄せる | 弱化・伸びる |
| 後頭下筋群 | 後頭部根本 | 頭の安定 | 頭痛の原因 |
| 胸鎖乳突筋 | 首側面 | 首を回す | 短縮・疲労 |
| 大胸筋 | 胸前面 | 腕を内側へ | 短縮（巻き肩） |
| 小胸筋 | 大胸筋の下 | 肩甲骨を前へ | 短縮（猫背） |

### 「肩こり」の正体

```
[原因]
 デスクワーク・スマホ姿勢
   ↓
[結果1] 大胸筋・小胸筋の短縮 (前傾姿勢)
[結果2] 菱形筋・僧帽筋下部の伸長・弱化
[結果3] 僧帽筋上部・肩甲挙筋の慢性的な収縮
[結果4] 後頭下筋群の緊張 (頭の安定維持)
   ↓
[症状] 肩こり・首こり・緊張型頭痛
```

→ つまり **「凝っている部分を揉むだけでは不十分」**。短縮した胸の前と、弱化した背中の中央もケアする必要があります。

