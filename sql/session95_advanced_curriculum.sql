# 腰部の集中ケア（慢性腰痛アプローチ）

腰痛は日本人の国民病。施術業として安全かつ効果的な腰部アプローチを学びます。

## 1. 腰痛の分類と施術可能性

### 施術業として「対応できる」腰痛
- ✅ デスクワーク・運動不足による筋肉性腰痛
- ✅ 立ち仕事による疲労腰痛
- ✅ ストレス・冷えによる腰痛
- ✅ 姿勢の悪さによる慢性腰痛

### 施術業として「対応できない」腰痛
- ❌ ヘルニア・脊柱管狭窄症（医療領域）
- ❌ 内臓由来の腰痛（腎臓・婦人科疾患）
- ❌ 急性のぎっくり腰直後
- ❌ 骨折・骨粗しょう症
- ❌ 妊娠中の腰痛（要医師確認）

施術前に **「医療機関での診断を受けたことがあるか」** を必ず確認します。

## 2. 腰部の解剖学的理解

### 主な原因筋

| 筋肉 | 位置 | 役割 |
|---|---|---|
| 脊柱起立筋 | 背骨両脇に縦に走る | 姿勢維持の主役 |
| 腰方形筋 | 腰の側面（深部） | 体側を傾ける |
| 大殿筋 | お尻 | 股関節を伸ばす |
| 中殿筋 | お尻の側面 | 股関節を外側に |
| 梨状筋 | 大殿筋の下（深部） | 股関節の外旋 |
| 腸腰筋 | お腹側の深部 | 股関節を曲げる |

### 「腰が痛い」の真の原因

```
[症状] 腰が痛い
   ↓
[実は]
- 脊柱起立筋の過緊張？  → 仕事の疲労
- 大殿筋の弱化？        → 運動不足
- 梨状筋の緊張？        → 坐骨神経痛様症状
- 腸腰筋の短縮？        → 反り腰
- 腰方形筋の緊張？      → 体側の歪み
```

→ どこが原因かを **触診で見極める** 力が必要です。

