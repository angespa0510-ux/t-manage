### ⚠️ 大切なルール
退職したセラピストは「ゴミ箱」に移動しないでください。
ステータスを「退職」に変更するだけでOKです。

### なぜゴミ箱に移動してはいけないの？
ゴミ箱に移動すると、そのセラピストに関連する過去の売上データや予約履歴で「不明なセラピスト」と表示されてしまいます。
退職ステータスのまま残しておけば、過去のデータも正しく表示され続けます。

### ゴミ箱はどんな時に使う？
・テストで作成しただけのセラピスト
・間違って重複登録してしまったセラピスト
・一度も予約や売上がないセラピスト

### ゴミ箱の仕組み
・削除ボタン → ゴミ箱に移動（すぐには消えない）
・30日以内なら「🔄 復元」ボタンで元に戻せる
・30日経過すると自動で完全削除される

### 正しい運用まとめ
・退職 → ステータスを「退職」に変更（データは残る）
・テストデータ → ゴミ箱に移動（30日で自動削除）

### パフォーマンスについて
セラピストが1,000人以上登録されていてもパフォーマンスは落ちませんのでご安心ください。
データベース（PostgreSQL）は数万件でも軽快に動作します。
もしデータが重くなったと感じたら、開発チーム（Claude）にご相談ください。DB最適化やインデックス追加で対応できます。', 4),

(4, '業務委託契約書の発行と署名', '## 契約書の電子署名

新しいセラピストが入店する際、業務委託契約書に電子署名してもらいます。

### 契約書リンクの発行方法
① セラピスト管理画面を開く
② 対象セラピストのカードで「❌ 未契約」をタップ
③ URLが自動でクリップボードにコピーされる
④ LINEでセラピストにURLを送る

### セラピスト側の操作（スマホ）
① LINEで届いたURLを開く
② 業務委託契約書の内容を確認
③ 氏名・住所を入力
④ 署名エリアに指でサインする
⑤ 「署名して契約に同意する」ボタンをタップ
⑥ 完了画面が表示される

### 署名完了後
・カード一覧のバッジが「✅ 契約済」に変わる
・詳細画面にも「✅ 契約済み」と署名日が表示される

### 契約書の内容
・委託業務（リラクゼーション施術）
・業務遂行の基準
・報酬規定
・秘密保持義務
・契約期間（1年・自動更新）
・解除条件

### 注意事項
・1人につき1つのリンクが発行されます
・セラピストはログイン不要（トークン認証）
・署名は1回限り（署名後はリンクが無効化）', 5),

